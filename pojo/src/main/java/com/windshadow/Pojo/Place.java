package com.windshadow.Pojo;

public class Place {
    //座位表

    private int id;//座位ID
    private int line;//座位行
    private int column;//座位列

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getLine() {
        return line;
    }

    public void setLine(int line) {
        this.line = line;
    }

    public int getColumn() {
        return column;
    }

    public void setColumn(int column) {
        this.column = column;
    }
}
